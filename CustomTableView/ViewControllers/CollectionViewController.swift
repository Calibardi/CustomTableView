import UIKit

class CollectionViewController: UICollectionViewController {

    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        title = "Collection View Semplice"
        // Register cell classes
        initCell()

    }
    
    deinit {
        debugPrintString("deInit called on collectionViewController!")
    }

    private func initCell() {
        let collectionCellNib = UINib(nibName: CustomCollectionViewCell.identifier, bundle: nil)
        self.collectionView.register(collectionCellNib, forCellWithReuseIdentifier: CustomCollectionViewCell.identifier)
    }

    // MARK: UICollectionViewDataSource

    override func numberOfSections(in collectionView: UICollectionView) -> Int {
        return 1
    }


    override func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return mergedColors.count
    }

    override func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        guard let cell = collectionView.dequeueReusableCell(withReuseIdentifier: CustomCollectionViewCell.identifier, for: indexPath) as? CustomCollectionViewCell else {
            return UICollectionViewCell()
        }
    
        // Configure and return the cell
        cell.fillCell(with: mergedColors[indexPath.row])
        return cell
    }

